var numbers = [29, 34, 48, 52];
console.log(numbers);

// add an element at the end inside the numbers array

numbers.push(105, 209, 'hello');
console.log(numbers);

// delete the last element inside the numbers array 

numbers.pop('hello');
console.log(numbers);

var peopleName = ['ami', 'tumi', 'apni', 'tini'];
//add an element at first inside the name array

peopleName.unshift('janina');
console.log(peopleName);

//Remove the first element for an array to using shift
peopleName.shift();
console.log(peopleName);

